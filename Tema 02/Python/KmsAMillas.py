# Escribir un algoritmo que pida un número de Kms e nos visualice o seu valor en millas
#
#
#  Pedir Kms
#  Millas = Kms/1.60934
#  Visualizar Millas
kms=input("Introduce un número: ")
millas=float(kms)/1.60934
print(str(kms)+" Kms son "+str(millas)+" Millas")
